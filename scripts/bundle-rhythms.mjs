#!/usr/bin/env node
/**
 * bundle-rhythms.mjs — generate rhythms/bundle.js for synchronous startup loading.
 *
 * The editor includes this file via <script src="../rhythms/bundle.js">,
 * making rhythm patterns available synchronously at startup (including file://).
 *
 * Run after editing any .hat.txt file or updating index.json:
 *   node scripts/bundle-rhythms.mjs
 */
import { readFileSync, writeFileSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');
const index = JSON.parse(readFileSync(join(ROOT, 'rhythms/index.json'), 'utf8'));

const patterns = [];
let nMissing = 0;

for (const entry of index) {
  const filePath = join(ROOT, 'rhythms', entry.file);
  try {
    const hat = readFileSync(filePath, 'utf8');
    patterns.push({ cat: entry.cat, name: entry.name, time: entry.time, grid: entry.grid, hat });
  } catch {
    console.warn(`SKIP (missing): ${entry.file}`);
    nMissing++;
  }
}

const js =
`// Generated by scripts/bundle-rhythms.mjs — do not edit manually.
// Regenerate: node scripts/bundle-rhythms.mjs
// Loaded by src/editor.html via <script src="../rhythms/bundle.js">.
/* global window */
window.RHYTHM_LIBRARY = ${JSON.stringify(patterns, null, 2)};
`;

writeFileSync(join(ROOT, 'rhythms/bundle.js'), js, 'utf8');
console.log(`bundle.js: ${patterns.length} patterns written${nMissing ? `, ${nMissing} skipped` : ''}`);
